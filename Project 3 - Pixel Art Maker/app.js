  // ctrlMakeGrid is a private function, only accessible by the appController module. The function is the central engine in creating all USER grids.
  const ctrlMakeGrid = function() {
    let input, userGrid;

    // 1. Get the input field data from the UI

    // calls the getInput METHOD in the UI module which returns an object with 4 key pair values: name, height, width, pxColor and stores the object in the variable "input"
    input = UICtrl.getInput();

    // 2. get the grid Object from the dataController

    // call the makeNewGrid METHOD in the dataController Module passing it the users input data of name, height, and width. The makeNewGrid Method then stores the data in the data Structure, manipulates it and returns two new objects. The first object is the name, height and width of the user canvas, the second is a gridHash which represents the id and background color, in a key/pair value, of every single cell in the the user Canvas (table#pixel_canvas), This two objects are then stored in userGrid
    userGrid = dataCtrl.makeNewGrid( input.name, input.height, input.width );

    // 3. display the new grid in the UI

    // calls the makeGrid Method located in the UI module and passes it the two objects stored within the userGrid variable
    UICtrl.makeGrid( userGrid );

    // 4. set canvas EventListeners

    // calls the appControllers PRIVATE function (paintcells), to set the event listeners on the new created canvas (table#pixel_canvas)
    listenForCanvasClicks();

    // 5. ctrlMakeGrid returns false to prevent the default action of refeshing the page from the form submission
    return false;
  };




